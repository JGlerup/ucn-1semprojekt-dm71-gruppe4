    private void btnCustomerCreateCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerCreateCustomerActionPerformed
        // TODO add your handling code here:
        try {
            String customerName = txtCreateCustomerName.getText();
            String customerAddress = txtCreateCustomerAddress.getText();
            String customerPhone = txtCreateCustomerPhone.getText();
            JTextField[] txtFields = {txtCreateCustomerName,
                txtCreateCustomerAddress, txtCreateCustomerPhone};
            if (customerName.trim().isEmpty() || customerAddress.trim().isEmpty() || customerPhone.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Udfyld venligst alle felterne");
            } else {
                int customerID = customerCtr.createCustomer(customerName, customerAddress, customerPhone);
                updateCustomerList();
                resetFields(txtFields);
                JOptionPane.showMessageDialog(this, "En medarbejder med ID'et " + customerID);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fejl: " + e.getMessage());
        }
}//GEN-LAST:event_btnCustomerCreateCustomerActionPerformed

    private void btnDeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCustomerActionPerformed
        // TODO add your handling code here:
        int customerID = 0;
        try {
            customerID = Integer.parseInt(txtDeleteCustomer.getText());
            if (customerCtr.getCustomer(customerID) != null) {
                customerCtr.deleteCustomer(customerID);
                updateCustomerList();
                txtDeleteCustomer.setText("");
                //Efterfølgende er for at nulstille opdater vare vinduet.
                //Dette forhindrer brugeren at opdaterer en vare,
                //som ikke eksisterer.
                JTextField[] txtFields = {txtUpdateCustomerName, txtUpdateCustomerAddress,
                    txtUpdateCustomerPhone};
                resetFields(txtFields);
                disableFields(txtFields);
                txtUpdateCustomerGetCustomer.setText("");
                JOptionPane.showMessageDialog(this, "Varen med ID'et " + customerID + " blev slettet");

            } else {

                JOptionPane.showMessageDialog(this, "ID'et " + customerID + " blev ikke fundet");
                txtDeleteCustomer.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Skal være et heltal");
            txtDeleteCustomer.setText("");
        }
}//GEN-LAST:event_btnDeleteCustomerActionPerformed

