    private void btnUpdateCustomerGetCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateCustomerGetCustomerActionPerformed
        // TODO add your handling code here:
        try {
            int customerID = Integer.parseInt(txtUpdateCustomerGetCustomer.getText());
            if (customerCtr.getCustomer(customerID) != null) {
                customerIDUpdate = customerID;
                JTextField[] txtFields = {txtUpdateCustomerName, txtUpdateCustomerAddress,
                    txtUpdateCustomerPhone};
                enableFields(txtFields);
                txtUpdateCustomerName.setText(customerCtr.getCustomer(customerID).getName());
                txtUpdateCustomerAddress.setText(customerCtr.getCustomer(customerID).getAddress());
                txtUpdateCustomerPhone.setText(customerCtr.getCustomer(customerID).getPhone());
            } else {
                JOptionPane.showMessageDialog(this, "ID'et blev ikke fundet");
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Skal være et heltal");
        }
}//GEN-LAST:event_btnUpdateCustomerGetCustomerActionPerformed

