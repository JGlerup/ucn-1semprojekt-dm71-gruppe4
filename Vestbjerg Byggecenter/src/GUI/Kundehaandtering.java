    private void btnCustomerSeachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerSeachActionPerformed
        // TODO add your handling code here:
        try {
            int customerID = Integer.parseInt(txtCustomerSearch.getText());
            if (customerCtr.getCustomer(customerID) != null) {
                customerCtr.getCustomer(customerID);
                txtCustomerSearch.setText("");
                int rows = tblCustomerList.getModel().getRowCount();
                int col = 0;
                for (int i = 0; i < rows; i++) {
                    int value = (Integer) tblCustomerList.getModel().getValueAt(i, col);
                    if (value == customerID) {
                        tblCustomerList.setRowSelectionInterval(i, i);
                        tblCustomerList.addRowSelectionInterval(i, i);
                        tblCustomerList.scrollRectToVisible(tblCustomerList.getCellRect(i, i, true));
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "ID'et " + customerID + " blev ikke fundet");
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Skal vÃ¦re et heltal");
            txtCustomerSearch.setText("");
        }
}//GEN-LAST:event_btnCustomerSeachActionPerformed

