    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        StringBuffer b = new StringBuffer();
        int itemID = Integer.parseInt(txtItemID.getText());
        if(itemCtr.getItem(itemID) != null) {
            b.append("Varenavn: " + itemCtr.getItem(itemID).getItemName() + "\n");
            b.append("Pris: " + itemCtr.getItem(itemID).getItemPrice() + "\n");
            b.append("Beskrivelse: " + itemCtr.getItem(itemID).getDescription() + "\n");
            b.append("Maksimumsbeholdning: " + itemCtr.getItem(itemID).getMaxInStock() + "\n");
            b.append("Minimumsbeholdning: " + itemCtr.getItem(itemID).getMinInStock() + "\n");
            b.append("Antal: " + itemCtr.getItem(itemID).getItemsInStock() + "\n");
            b.append("Brand: " + itemCtr.getItem(itemID).getBrand() + "\n");
            txtItemDetails.setText(b.toString());
        } else {
            JOptionPane.showMessageDialog(this, "ID'et blev ikke fundet");
        }
}//GEN-LAST:event_jButton2ActionPerformed

