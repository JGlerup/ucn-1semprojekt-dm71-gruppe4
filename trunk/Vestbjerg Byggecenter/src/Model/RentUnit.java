package Model;

import java.util.Date;


public class RentUnit
{
    private int serial;
    private String date;


public RentUnit()
{
    serial = 0;
    date = null;
}

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getSerial() {
        return serial;
    }

    public void setSerial(int serial) {
        this.serial = serial;
    }








}